// Compara dois valores e resulta, SEMPRE, em true e false. 

// igualdade, comparando somente o valor | sinal ==
console.log('1' == 1)

// estritamente igual, além do valor compara também o tipo | sinal ===
console.log('1' === 1)

// diferente, somente valor | sinal !=
console.log('3' != 3)

// estritamente diferente | sinal !==
console.log('3' !== 3)

// menor que | sina <
console.log(3 < 2)

// maior que | sinal >
console.log(2 > 0)

// menor ou igual | sinal <=
console.log(5 <= 4)

// maior ou igual | sinal >=
console.log(5 >= 5)

