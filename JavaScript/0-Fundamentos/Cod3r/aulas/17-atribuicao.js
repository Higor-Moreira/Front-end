// Existem algumas variações para o sinal de atribuição. 

// Tradicional 
let a = 5
let b = 3


// atribuição aditiva | a recebe a + b 
console.log(a += b)


// atribuição subtrativa | a recebe a - 3
console.log(a -= 3)


// atribuição multiplicativa | b recebe b vezes 2
console.log(b *= 2)


// atribução divisiva | b recebe b dividido por 2
console.log(b /= 2)

